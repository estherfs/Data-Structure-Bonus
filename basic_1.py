#印出四位數
s0 = ["*****", "*   *", "*   *", "*   *", "*****"]
s1 = ["    *", "    *", "    *", "    *", "    *"]
s2 = ["*****", "    *", "*****", "*    ", "*****"]
s3 = ["*****", "    *", "*****", "    *", "*****"]
s4 = ["*   *", "*   *", "*****", "    *", "    *"]
s5 = ["*****", "*    ", "*****", "    *", "*****"]
s6 = ["*    ", "*    ", "*****", "*   *", "*****"]
s7 = ["*****", "    *", "    *", "    *", "    *"]
s8 = ["*****", "*   *", "*****", "*   *", "*****"]
s9 = ["*****", "*   *", "*****", "    *", "    *"]
s = [s0, s1, s2, s3, s4, s5, s6, s7 ,s8, s9]
num = input()

for row in range(5):
    for col in range(len(num)):
        if(col):
            print('', end=' ')
        print(s[int(num[col])][row], end='')
    print()
